using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using TroTot_Admin.Infrastructure;
using TroTot_Admin.Model;

namespace TroTot_Admin.Pages.Account
{
    public class DetailsModel : PageModel
    {
        private readonly TroTot_Admin.Infrastructure.DataContext _context;

        public DetailsModel(TroTot_Admin.Infrastructure.DataContext context)
        {
            _context = context;
        }

      public User User { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.User == null)
            {
                return NotFound();
            }

            var user = await _context.User.FirstOrDefaultAsync(m => m.user_id == id);
            if (user == null)
            {
                return NotFound();
            }
            else 
            {
                User = user;
            }
            return Page();
        }
    }
}
